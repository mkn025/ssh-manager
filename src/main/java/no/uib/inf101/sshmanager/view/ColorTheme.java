package no.uib.inf101.sshmanager.view;

import java.awt.Color;



public class ColorTheme {
    private final Color backgroundColor = new Color(30, 32, 51);
    private final Color foregroundColor = new Color(41, 223, 228);
    private final Color accentColor = new Color(86, 182, 194);
    private final Color sidebarColor = new Color(41, 43, 61);
    private final Color textColor = Color.WHITE;

    public Color getBackgroundColor() { return backgroundColor; }
    public Color getLabelInfoColor() { return foregroundColor; }
    public Color getAccentColor() { return accentColor; }
    public Color getSidebarColor() { return sidebarColor; }
    public Color getTextColor() { return textColor; }


}
